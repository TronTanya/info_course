function closing(): string {
  return [
    "## После лекции",
    "",
    "- Пройдите тест по модулю.",
    "- Откройте практическое задание; при затруднении используйте подсказки и чат помощника (без готовых ответов на зачёт).",
    "",
    "> Материал учебный. Если правила в вашей организации жёстче — следуйте им.",
  ].join("\n");
}


const SURFACE_STRICTNESS: Partial<Record<MentorSurface, string>> = {
  practice: [
    "# Контекст: практическая лаборатория",
    "Отвечай особенно осторожно: только сократические подсказки, без эталона, без флагов, без команд эксплуатации.",
  ].join("\n"),
  test_result: [
    "# Контекст: после контрольного теста",
    "Не раскрывай правильные ответы, варианты «верно/неверно» по пунктам и разбор конкретных вопросов с ключами.",
    "Фокус на понимании темы и стратегии подготовки.",
  ].join("\n"),
};

export function buildMentorModeSystemBlock(modeId: AIMentorMode, surface: MentorSurface): string {
  const blocks = [MODE_POLICY[modeId], SURFACE_STRICTNESS[surface]].filter(Boolean);
  return blocks.join("\n\n");
}

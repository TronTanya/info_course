import type { TutorRefusalCode } from "@/lib/ai/tutor/types";

/** Безопасные шаблоны отказа — без утечки системного промпта и без поощрения обхода. */
export const REFUSAL_TEMPLATES: Record<TutorRefusalCode, string> = {
  prompt_injection: [
    "Я учебный наставник CyberEdu и не меняю свои правила по просьбе в чате.",
    "",
    "Переформулируйте вопрос **обычным языком** — по теме лекции, практики или защиты информации. Например: «Как распознать …» или «Что проверить в …».",
  ].join("\n"),

  policy_blocked: [
    "Этот запрос не подходит для учебного чата курса по информационной безопасности.",
    "",
    "Задайте вопрос по **материалам модуля**, **практике** или по **защите** в легальном контексте — без инструкций для атак.",
  ].join("\n"),

  offensive_attack: [
    "Я не даю пошаговых инструкций для взлома, эксплойтов или обхода защит.",
    "",
    "Могу помочь в учебном формате:",
    "- как **распознать** угрозу;",
    "- какие **меры защиты** применяют;",
    "- типичные **ошибки пользователей** и как их избежать.",
  ].join("\n"),

  exam_spoiler: [
    "Готовые ответы на тесты и практику я не выдаю — это часть учебной честности.",
    "",
    "Давайте разберём **идею задания**: что уже понятно, какой **первый шаг** вы можете сделать сами за 5–10 минут, и где застряли.",
  ].join("\n"),

  output_blocked: [
    "Ответ не прошёл проверку безопасности. Попробуйте переформулировать вопрос короче и по теме урока.",
  ].join("\n"),

  provider_error: [
    "Сервис наставника временно недоступен. Попробуйте через несколько минут.",
  ].join("\n"),
};

export function getRefusalTemplate(code: TutorRefusalCode): string {
  return REFUSAL_TEMPLATES[code] ?? REFUSAL_TEMPLATES.policy_blocked;
}

/** Сохранять пару user+assistant в server history после отказа (для связности диалога). */
export function shouldPersistRefusalInHistory(code: TutorRefusalCode): boolean {
  return code === "exam_spoiler" || code === "offensive_attack";
}

export function buildMentorRefusalStructured(
  kind: MentorRefusalKind,
  opts?: MentorRefusalCopyOpts,
): MentorRefusalStructured {
  const topic = topicLabel(opts);
  return STRUCTURED[kind](topic);
}

/** Текст ответа в чате (markdown, без технических деталей). */
export function formatMentorRefusalMessage(structured: MentorRefusalStructured): string {
  return [
    structured.denial,
    "",
    `**Почему:** ${structured.reason}`,
    "",
    `**Чем могу помочь:** ${structured.alternative}`,
    "",
    `**Следующий шаг:** ${structured.learnAction}`,
  ].join("\n");
}
